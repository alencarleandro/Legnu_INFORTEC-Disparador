package br.com.legnu_propagar.model;

public class TbCategoria {

	private int id;
	private String nome;
	private String descrixcao;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getDescrixcao() {
		return descrixcao;
	}

	public void setDescrixcao(String descrixcao) {
		this.descrixcao = descrixcao;
	}

}
